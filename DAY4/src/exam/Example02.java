package exam;

public class Example02 {
	public static void main(String[] args) {
		/*
		 * 관계연산자 (<, <=, >, >=, ==, !=)
		 */
		int a = 10;
		int b = 20;
		System.out.println(a > b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
		
		char ch1 = 'A';
		int num1 = 65;
		System.out.println(ch1 == num1);
		
	}
}
