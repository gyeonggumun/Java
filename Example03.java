package exam;

public class Example03 {
	public static void main(String[] args) {
		/*
		 * 실행문
		 * - 실행문은 변수의 선언 및 변수 값 저장, 메소드 호출에 해당하는 코드를 말함
		 * - 실행문 끝에는 반드시 세미콜론(;)을 붙혀서 실행문이 끝났음을 표시해야 한다
		 */
		// 한 줄에 여러개의 실행문 작성
		int a1 = 10; int a2 = 20; int a3 = 30;
		System.out.println(a1);
		System.out.println(a2); System.out.println(a3);
	
		// 한 줄에 하나의 실행문만 작성
		int b1 = 10;
		int b2 = 20;
		System.out.println(b1);
		System.out.println(b2);
		
		
	}

}
